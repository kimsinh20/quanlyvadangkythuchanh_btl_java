    private void buttonDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDoiActionPerformed
        // TODO add your handling code here:
        String mkc = matKhauCu.getText();
        String mkm = matKhauMoi.getText();
        String nlmk = nhapLaiMatKhau.getText();

        if (!mkm.equals(nlmk)) {
            System.out.println("Mật khẩu không khớp.");
            return;
        }
        
        if (DBQuanLyThucHanh.getLogin(maGV, mkc) == 1) {
            DBQuanLyThucHanh.updateMatKhau(maGV, mkm);
            JOptionPane.showMessageDialog(rootPane, "Đổi mật khẩu thành công.");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu sai.");
        }

    }//GEN-LAST:event_buttonDoiActionPerformed

